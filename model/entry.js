


class entry{
    _name = ""
    _entryId = ""
    _title = ""
    _body = ""
    _date = ''

    get name() {
        return this._name;
    }

    set name(value) {
        this._name = value;
    }

    get entryId() {
        return this._entryId;
    }

    set entryId(value) {
        this._entryId = value;
    }

    get title() {
        return this._title;
    }

    set title(value) {
        this._title = value;
    }

    get body() {
        return this._body;
    }

    set body(value) {
        this._body = value;
    }

    get date() {
        return this._date;
    }

    set date(value) {
        this._date = value;
    }
}
